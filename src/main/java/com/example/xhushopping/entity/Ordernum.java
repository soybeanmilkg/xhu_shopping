package com.example.xhushopping.entity;

public class Ordernum {
    private int ordernum_id;
    private String ordernum_time;
    private int ordernum_num;

    public int getOrdernum_id() {
        return ordernum_id;
    }

    public void setOrdernum_id(int ordernum_id) {
        this.ordernum_id = ordernum_id;
    }

    public String getOrdernum_time() {
        return ordernum_time;
    }

    public void setOrdernum_time(String ordernum_time) {
        this.ordernum_time = ordernum_time;
    }

    public int getOrdernum_num() {
        return ordernum_num;
    }

    public void setOrdernum_num(int ordernum_num) {
        this.ordernum_num = ordernum_num;
    }
}
